print("Boas Vindas ao Calculador de Inteiros!")
prim_valor =int(input("Por favor, informe o primeiro valor: "))
seg_valor =int(input("Por favor, informe o segundo valor: "))

soma=(prim_valor) + (seg_valor)
subrtracao = (prim_valor) - (seg_valor)
